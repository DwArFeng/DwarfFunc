/**
 * 配置开发包。
 * <p> 提供便捷的配置模板。
 * 
 * <p> <b>有关此配置模板：</b>
 * 
 * <br> 该配置模板能够实现：对配置的快捷读写；在程序中动态的设置配置，并且向感兴趣的对象对发放更改配置的通知；
 * 配置纠错以及对错误进行还原为默认配置的处理。
 * <br> 该配置模板的配置核心是映射式的，这个核心体系被称作配置映射体系。该体系下，所有的配置以键和值的形式被存放在
 * {@link com.dwarfeng.dutil.develop.cfg.ConfigPort} 中。
 * <br> 配置映射体系中有三个映射，分别是
 * <BLOCKQUOTE> 
 *		<code> Map&ltConfigKey. String&gt defaultValueMap</code> ―― 默认值映射<br>
 * 		<code> Map&ltConfigKey, String&gt currentValueMap </code> ―― 当前值映射 <br>
 * 		<code> Map&ltConfigKey, ConfigValueChecker&gt</code> ―― 值检查器映射
 * </BLOCKQUOTE>
 * 这三个映射是整个配置体系的核心：在一般情况下，一个键下，值检查器负责检测某个特定的值是否有效，当当前值有效时，当前值在发挥作用；当当前值无效时，默认值
 * 发挥作用；默认值还可以作为复位或者初始配置使用。<b>默认值必须是有效的值，而当前值可以有效也可以无效。</b>
 * 当当前值无效时，用户可以使用默认值替代，也可以使用其他的方法处理，比如不让程序继续运行或者抛出异常。
 * <p> <b> 有关线程的安全性：</b>
 * <p>该包中的所有对象都是线程不安全的――一般而言，配置操作并不需要多线程。 如果需要对配置进行多线程操作，请编写外部同步方法。
 * 
 * 
 * @author DwArFeng
 * @since 1.8
 */
